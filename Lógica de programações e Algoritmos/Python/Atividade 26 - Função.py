def soma(n1, n2):
    return (n1+n2)
def subtracao(n1, n2):
    return (n1-n2)

def multiplicacao(n1, n2):
    return (n1*n2)

def divisao(n1, n2):
    return (n1/n2)

print(soma(45, 48))
print(soma)

print(subtracao(77, 42))
print(subtracao)

print(multiplicacao(7, 4))
print(multiplicacao)

print(divisao(52, 4))
print(divisao)

